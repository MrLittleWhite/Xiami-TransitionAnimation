#import "UIView+Layout.h"

@implementation UIView (Layout)

+ (CGFloat)screenWidth{
    return [UIScreen mainScreen].bounds.size.width;
}

+ (CGFloat)screenHeight{
    return [UIScreen mainScreen].bounds.size.height;
}


@end
